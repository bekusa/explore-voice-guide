interface LangContextValue {
  lang: LangCode;
  setLang: (l: LangCode) => void;
  t: (key: TKey) => string;
  meta: LanguageMeta;
}

const LangContext = createContext<LangContextValue | null>(null);

const STORAGE_KEY = "lokali.lang";
const LAUNCH_KEY = "lokali.launched";

function detectInitial(): LangCode {
  if (typeof window === "undefined") return "ka";
  const saved = window.localStorage.getItem(STORAGE_KEY) as LangCode | null;
  if (saved && DICTIONARIES[saved]) return saved;
  return "ka";
}

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<LangCode>("ka");

  // Hydrate from localStorage once on client
  useEffect(() => {
    setLangState(detectInitial());
  }, []);

  const setLang = (l: LangCode) => {
    setLangState(l);
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, l);
    }
  };

  const value = useMemo<LangContextValue>(() => {
    const meta = LANGUAGES.find((l) => l.code === lang) ?? LANGUAGES[0];
    const dict = DICTIONARIES[lang] ?? en;
    return {
      lang,
      setLang,
      meta,
      t: (key) => dict[key] ?? en[key] ?? key,
    };
  }, [lang]);

  // Apply RTL & lang attribute
  useEffect(() => {
    if (typeof document === "undefined") return;
    const meta = LANGUAGES.find((l) => l.code === lang);
    document.documentElement.lang = lang;
    document.documentElement.dir = meta?.rtl ? "rtl" : "ltr";
  }, [lang]);

  return <LangContext.Provider value={value}>{children}</LangContext.Provider>;
}

export function useT() {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useT must be used inside <LanguageProvider>");
  return ctx;
}

export function hasFirstLaunched(): boolean {
  if (typeof window === "undefined") return true;
  return !!window.localStorage.getItem(STORAGE_KEY);
}
